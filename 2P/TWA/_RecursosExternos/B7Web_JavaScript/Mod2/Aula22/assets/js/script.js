// 1. Faça um loop que mostre todas as frutas
let fruits = ["Maçã", "Laranja", "Uva"];
for (let i in fruits) {
    console.log(fruits[i]);
}

// 2. Conte de 1 até 100 através de um WHILE
let contagem = "";
let cont = 1;
while (cont <= 100) {
    contagem += cont + " ";
    cont++;
}

console.log(contagem);