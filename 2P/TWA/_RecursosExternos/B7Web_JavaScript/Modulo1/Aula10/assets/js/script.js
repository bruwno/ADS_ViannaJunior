// let -> Permite modificação em tempo de execução.
let nome = "William";
nome = "William Bruno";
console.log(nome);

// var -> Faz o mesmo que o let, no entanto o var funciona em escopo global.
var graduacao = "Análise e Desenvolvimento de Sistemas";
graduacao = "ADS";
console.log(graduacao);

// const -> É uma constante e só pode ser modificada em tempo de desenvolvimento.
const anoNascimento = 1997;
console.log(anoNascimento);