    private void carregarListaDeEspecializacao() {
        for (EEspecializacao especializacao : EEspecializacao.values()) {
            cboEspecialidade.addItem(String.valueOf(especializacao));
        }
    }

    private Instrutor obterValidarDados() {
        String nome = this.txtNome.getText();
        String sobrenome = this.txtSobrenome.getText();
        String cpf = txtCpf.getText().replaceAll("[^\\d]", "");
        String email = this.txtEmail.getText();
        String dataNascimento = this.txtDataNascimento.getText();
        LocalDateTime dataRegistro = LocalDateTime.now();
        String login = txtLogin.getText();
        char[] senhaDigitada = txtSenha.getPassword();
        String senha = new String(senhaDigitada);
        String salarioStr = txtSalario.getText();
        String especializacaoSelecionada = (String) this.cboEspecialidade.getSelectedItem();
        EEspecializacao especializacao = getEspecializacao(especializacaoSelecionada);

        if (nome.isBlank() || sobrenome.isBlank() || cpf.isBlank() || email.isBlank() || login.isBlank() || senha.isBlank() || salarioStr.isBlank() || dataNascimento.isBlank()) {
            JOptionPane.showMessageDialog(rootPane, "Todos os campos devem ser preenchidos!", "Preenchimento inválido!", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        double salario;
        try {
            salario = Double.parseDouble(salarioStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "O campo Salário deve conter um número válido!", "Preenchimento inválido!", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        return new Instrutor(1, nome, sobrenome, cpf, email, login, senha, salario, especializacao, dataNascimento, dataRegistro);
    }

    private void realizarCadastro() {
        Instrutor instrutor = obterValidarDados();
        if (instrutor != null) {
            try {
                new InstrutorDAO().inserir(instrutor);
                JOptionPane.showMessageDialog(rootPane, "Cadastro realizado com sucesso!", "O usuário foi cadastrado", JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível realizar o cadastro.\nERRO: " + ex.getMessage(), "Ocorreu um erro", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    private void realizarAlteracaoNoCadastro() {
        Instrutor instrutor = obterValidarDados();
        if (instrutor != null) {
            int idInstrutor = Integer.parseInt(txtId.getText());
            instrutor.setId(idInstrutor);

            try {
                new InstrutorDAO().alterar(instrutor);
                JOptionPane.showMessageDialog(rootPane, "Cadastro alterado com sucesso!", "O usuário foi alterado", JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível realizar a alteração.\nERRO: " + ex.getMessage(), "Ocorreu um erro", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    private static EEspecializacao getEspecializacao(String especializacaoSelecionada) {
        EEspecializacao especializacao = null;
        for (EEspecializacao e : EEspecializacao.values()) {
            if (e.toString().equals(especializacaoSelecionada)) {
                especializacao = e;
                break;
            }
        }
        return especializacao;
    }

    public void realizarBuscaDeCadastroPorId() {
        Instrutor instrutor = null;
        try {
            instrutor = new InstrutorDAO().buscarPorId(Integer.valueOf(txtBuscarPorId.getText()));
        } catch (SQLException | ClassNotFoundException ex) {
            exibeMensagemErroAoBuscarNaBase(ex);
        }
        if (instrutor == null) {
            JOptionPane.showMessageDialog(rootPane, "A busca não retornou nenhum resultado!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            desabilitaAlteracaoDeCadastro();
        } else {
            preencherCampos(instrutor);
        }
    }

    private void preencherCampos(Instrutor instrutor) {
        habilitaAlteracaoDeCadastro();
        this.txtId.setText(String.valueOf(instrutor.getId()));
        this.txtNome.setText(instrutor.getNome());
        this.txtSobrenome.setText(instrutor.getSobrenome());
        this.txtEmail.setText(instrutor.getEmail());
        this.txtCpf.setText(instrutor.getCpf());
        this.txtDataNascimento.setText(FormataData.formataDataParaExibicao(instrutor.getDataNascimento()));
        this.txtSalario.setText(String.valueOf(instrutor.getSalario()));
        this.cboEspecialidade.setSelectedItem(instrutor.getEspecializacao().toString());
        this.txtLogin.setText(instrutor.getLogin());
        this.txtSenha.setText(instrutor.getSenha());
    }

    private void cancelarAlteracaoDeCadastro() {
        limparCampos();
        desabilitaAlteracaoDeCadastro();
    }

    private void habilitaAlteracaoDeCadastro() {
        this.btnCadastrar.setEnabled(false);
        this.btnAlterarDados.setEnabled(true);
        this.btnCancelar.setEnabled(true);
        limparCampos();
    }

    private void desabilitaAlteracaoDeCadastro() {
        this.btnCadastrar.setEnabled(true);
        this.btnAlterarDados.setEnabled(false);
        this.btnCancelar.setEnabled(false);
        limparCampos();
    }

    private void limparCampos() {
        this.txtNome.setText("");
        this.txtSobrenome.setText("");
        this.txtCpf.setText("");
        this.txtEmail.setText("");
        this.txtDataNascimento.setText("");
        this.txtLogin.setText("");
        this.txtSenha.setText("");
        this.txtSalario.setText("0.00");
        this.cboEspecialidade.setSelectedIndex(0);
    }

    private void exibeMensagemErroAoBuscarNaBase(Exception ex) {
        JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro ao tentar buscar os dados na base.\nERRO: " + ex.getMessage(), "Ocorreu um erro", JOptionPane.ERROR_MESSAGE);
    }

