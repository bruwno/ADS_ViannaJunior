    private void deletar() {
        try {
            if (this.getParent() instanceof ViewDetalhesAtividades) {
                new AtividadeDAO().apagar(Integer.parseInt(txtIdItemSelecionado.getText()));
                exibeMensagemDeSucessoNaExclusao();
                this.dispose();
            }
            else if (this.getParent() instanceof ViewDetalhesEventos) {
                new EventoDAO().apagar(Integer.parseInt(txtIdItemSelecionado.getText()));
                exibeMensagemDeSucessoNaExclusao();
                this.dispose();
            } else if (this.getParent() instanceof ViewDetalhesAdministradores) {
                new AdministradorDAO().apagar(Integer.parseInt(txtIdItemSelecionado.getText()));
                exibeMensagemDeSucessoNaExclusao();
                this.dispose();
            } else if (this.getParent() instanceof ViewDetalhesInstrutores) {
                new InstrutorDAO().apagar(Integer.parseInt(txtIdItemSelecionado.getText()));
                exibeMensagemDeSucessoNaExclusao();
                this.dispose();
            } else if (this.getParent() instanceof ViewDetalhesMembros) {
                new MembroDAO().apagar(Integer.parseInt(txtIdItemSelecionado.getText()));
                exibeMensagemDeSucessoNaExclusao();
                this.dispose();
            }
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não foi possível executar a operação.\nERRO: " + ex.getMessage(), "Ocorreu um erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void exibeMensagemDeSucessoNaExclusao() {
        JOptionPane.showMessageDialog(rootPane, "Item excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    }

