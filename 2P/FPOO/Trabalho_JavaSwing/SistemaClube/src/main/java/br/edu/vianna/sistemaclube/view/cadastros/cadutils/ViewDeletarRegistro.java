    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        int opcaoSelecionada = JOptionPane.showConfirmDialog(rootPane, "Clique em SIM para deletar permanentemente o registro.\n" +
                "Clique em NÃO para voltar à tela anterior.", "Confirmar operação", JOptionPane.YES_NO_OPTION);
        if (opcaoSelecionada == JOptionPane.YES_OPTION) {
            deletar();
        } else {
            this.dispose();
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

